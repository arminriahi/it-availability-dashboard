# به پوشه پروژه بروید
cd مسیر/به/it-availability-dashboard

# مخزن Git را مقداردهی اولیه کنید
git init

# فایل‌های خود را اضافه کنید
git add .

# commit اولیه
git commit -m "✅ نسخه اولیه: داشبورد آماده‌به‌کاری سرویس‌ها"

# مخزن ریموت را تنظیم کنید (لینک خود را از صفحه GitHub کپی کنید)
git remote add origin https://github.com/نام-کاربری-شما/it-availability-dashboard.git

# کد را آپلود کنید
git branch -M main
git push -u origin main